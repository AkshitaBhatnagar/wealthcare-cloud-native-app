package com.wealth.wealthfinancialplan.watson;


public class FpInputInfo {

    private String gender;
    private int age;
    private double avgincome;
    private String married;
    private int spouseage;
    private double spourceavgincome;
    private int noofchildren;
    private double  investmentamount;

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getAvgincome() {
        return avgincome;
    }

    public void setAvgincome(double avgincome) {
        this.avgincome = avgincome;
    }

    public String getMarried() {
        return married;
    }

    public void setMarried(String married) {
        this.married = married;
    }

    public int getSpouseage() {
        return spouseage;
    }

    public void setSpouseage(int spouseage) {
        this.spouseage = spouseage;
    }

    public double getSpourceavgincome() {
        return spourceavgincome;
    }

    public void setSpourceavgincome(double spourceavgincome) {
        this.spourceavgincome = spourceavgincome;
    }

    public int getNoofchildren() {
        return noofchildren;
    }

    public void setNoofchildren(int noofchildren) {
        this.noofchildren = noofchildren;
    }

    public double getInvestmentamount() {
        return investmentamount;
    }

    public void setInvestmentamount(double investmentamount) {
        this.investmentamount = investmentamount;
    }
}
